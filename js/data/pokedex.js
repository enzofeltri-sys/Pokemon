  // comme dans les jeux originaux (ex: Ptéra rapporte de la Vitesse).
  const topStat = Object.keys(baseStats).reduce((a, b) => (baseStats[b] > baseStats[a] ? b : a));
  const statTotal = hp + atk + def + spa + spd + spe;
